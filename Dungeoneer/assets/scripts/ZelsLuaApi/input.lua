GameInput = {}

print("Setting up lua input");
-- Create a reference to the Java api
GameInput.input = luajava.newInstance("com.zel.lua.input.LuaInputHandler");
GameInput.keysPressed = {};
GameInput.keysReleased = {};

-- What happens when a key is pressed / released
function GameInput.OnKeyPressed(keyCode)
	GameInput.keysPressed[keyCode] = true;
	GameInput.keysReleased[keyCode] = false;
end

function GameInput.OnKeyReleased(keyCode)
	GameInput.keysPressed[keyCode] = false;
	GameInput.keysReleased[keyCode] = true;
end

function keyPressed(code)
	return GameInput.keysPressed[code] == true;
end

function keyReleased(code)
	return GameInput.keysReleased[code] == true;
end

